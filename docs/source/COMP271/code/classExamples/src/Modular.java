public class Modular {

    private int a;
    private int b;

    public Modular() {
        a = -1;
        b = -a;
    }

    public Modular(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public static void main(String[] args) {

        Modular[][] demo = new Modular[2][2];
    }
}
